package misc;

public class prefix1 {
    public static void main(String[] args) {
        int a  = 5 ;
        System.out.println(++a);  // 6
        System.out.println(a++); // 6
        System.out.println(a);  // 7
    
    int b= 3 ;
     System.out.println(b--); // 3
     System.out.println(--b); // 1

    }

}
